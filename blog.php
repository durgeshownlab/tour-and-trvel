											<p>Many people want a simple life away from all the chaos that seems
												self-inflicted. The first step to embracing this new form of lifestyle
												is to understand what simplicity means to you and then live by that
												definition. Here are some suggestions on how to live a simple life.</p>
											<h3>Slow Down</h3>
											<p>There are times when you are so used to doing everything in a hurry that
												you don’t notice how fast-paced your life has become. Simply reading the
												words, “Slow down,” will allow you to pause for a moment and notice.
												This step is mentioned first so you will carry the thought with you
												throughout this article and beyond.</p>
											<ul>
												<li>Avoid multi-tasking. It has become popular, if not clichéd, to
													multi-task. Research indicates that there is a point where the
													quality of what you are doing declines when you try to focus on too
													many tasks at once. Just because everyone is doing it doesn’t mean
													you have to.</li>
												<li>Find your threshold of diminishing return on the number of tasks you
													can work on. Your goal is to do things well so you can feel good
													about your accomplishments.</li>
												<li>Take frequent breaks while working.</li>
											</ul>
											<h3>Eliminate Extras</h3>
											<p>The concept of conspicuous consumption may describe your life. It entails
												lavish or wasteful spending that attempts to show others your level of
												social prestige. Simplifying your life will drastically cut into the
												amount of “extras” you have grown accustom to. The goal is to cut out
												the extra expenses so you won’t be tied down by financial obligations.
											</p>
											<ul>
												<li>Question if you really need that third ipad or newest electronic
													gadget, or twice-a-day trip through the coffee retailer drive-thru.
													Just say “no” to yourself, and “yes” to your desire to live a
													simpler and more peaceful life. Each time you are faced with a
													decision, you can make a good decision.</li>
												<li>Find fulfillment in the simple things in life by spending time with
													friends, in nature, or building something with your own two hands.
													Intrinsic rewards will improve your motivation and overall
													satisfaction with your life.</li>
											</ul>
											<h3>Practice Empathy and Compassion to Create Peace</h3>
											<p>The ability to appreciate someone else’s struggle is an important skill
												to develop. It comes easy to some people, and not to others. You know
												how you would like to be treated, so use that as a guide when trying to
												forgive someone.</p>
											<ul>
												<li>If you want to practice empathy and compassion start by reaching out
													to a family member or friend and offer to help her in some way.
													Perhaps you could run an errand for her, or do something simple like
													unload groceries, or water her plants. The point of this exercise is
													to provide to others the feelings and actions you appreciate when
													someone does the same for you.</li>
											</ul>
